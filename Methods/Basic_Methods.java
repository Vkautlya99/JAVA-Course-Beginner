
public class Basic_Methods{
    public static void main(String[] args) {
        greet();
        putName();
        putAge();
        putCity();
        putEducation();
        putContact();
    }
          // Below all the written code is Methods or you can call it function.
    static void greet() {
        System.out.println("Hello World !");
    }

    static void putAge() {
        System.out.println(22);
    }
    static void putName() {
        System.out.println("Vikram Kautlya");
    }
    static void putCity() {
        System.out.println("Pune, Maharastra");
    }
    static void putEducation() {
        System.out.println("Bachelor's in Technology.");
    }
    static void putContact() {
        System.out.println(1245677);
    }
}










