
// WAP in JAVA to print the name that is in the String array using For-each loop
public class second {
    public static void main(String[] args) {
        String names[] = { "Peter", "Vikram", "William Hill", "Laxita", "Rajat" };

        for (String name : names) {
            System.out.println("Hellow ! My name is : "+ name);
        }
    }
}

