public class RelationalOperator {
    public static void main(String[] args) {
        int firstNumber = 30;
        int secondNumber = 22;

        System.out.println(firstNumber == secondNumber);
        System.out.println(firstNumber > secondNumber);
        System.out.println(secondNumber < firstNumber);
        System.out.println(firstNumber >= secondNumber);
        System.out.println(secondNumber <= firstNumber);
        System.out.println(firstNumber != secondNumber);
    }
}
