public class Increment_DecrementOperator {
    public static void main(String[] args) {
        int a = 30;
        a++;
        System.out.println(a);

        int b = 22;
        b--;
        System.out.println(b);

        int c = 35;
        c = c + 1;
        c++;
        System.out.println(c);
        
    }
}
